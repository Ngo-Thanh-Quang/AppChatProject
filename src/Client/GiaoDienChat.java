    private void btnGuiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuiActionPerformed
        String messageContent = tfNhapTinNhan.getText();
        if(messageContent.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập tin nhắn");
            return;
        }
        if(JLuaChonNguoiNhan.getSelectedIndex()==0){
            try {
                write("send-to-global"+","+messageContent+","+this.id);
                VungXemTinNhan.setText(VungXemTinNhan.getText()+"Bạn: "+messageContent+"\n");
                VungXemTinNhan.setCaretPosition(VungXemTinNhan.getDocument().getLength());
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(rootPane, "Có lỗi xảy ra");
            }
        }else{
            try {
                String[] parner = ((String)JLuaChonNguoiNhan.getSelectedItem()).split(" ");
                write("send-to-person"+","+messageContent+","+this.id+","+parner[1]);
                VungXemTinNhan.setText(VungXemTinNhan.getText()+"Bạn (tới Client "+parner[1]+"): "+messageContent+"\n");
                VungXemTinNhan.setCaretPosition(VungXemTinNhan.getDocument().getLength());
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(rootPane, "Có lỗi xảy ra");
            }
        }
        tfNhapTinNhan.setText("");
    }//GEN-LAST:event_btnGuiActionPerformed

